/**
 * 获取图库数据
 */
function getGalleryData() {
  return GALLERY_DATA
}

/**
 * 获取品牌列表
 */
function getBrands() {
  var seen = {}
  return GALLERY_DATA.brands.filter(function(b) {
    if (seen[b.brand]) return false
    seen[b.brand] = true
    return true
  }).map(function(b) {
    return { key: b.brand, name: b.brandName }
  })
}

/**
 * 获取某品牌的所有车型分组
 * brandKey: 'bmw' | 'benz' | 'porsche'
 */
function getModels(brandKey) {
  return GALLERY_DATA.brands.filter(function(b) {
    return b.brand === brandKey
  })
}

/**
 * 生成 CDN URL
 * cdnPath: 如 "images/baomawaiguan/xxx.jpg"
 */
function resolveGalleryImageUrl(cdnPath) {
  if (!cdnPath) return ''
  if (cdnPath.indexOf('http') === 0) return cdnPath
  return 'https://img.hcmg.store/' + cdnPath
}

